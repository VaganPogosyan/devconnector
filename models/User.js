const mongoose = require('mongoose'); // get mongoose to create Schema

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

// mongoose.model takes in the name of the model and a Schema to create it
module.exports = User = mongoose.model('user', UserSchema);
